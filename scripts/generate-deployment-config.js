import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const configPath = path.join(__dirname, '..', 'public', 'config', 'deployment.json');
const outputPath = path.join(__dirname, '..', 'src', 'config', 'deployment-config.ts');

try {
  const configContent = fs.readFileSync(configPath, 'utf-8');
  const config = JSON.parse(configContent);

  const useWindowLocation = config.mode === 'location';
  const useNavigate = config.mode === 'navigate' || config.mode !== 'location';

  const tsContent = `// Auto-generated deployment configuration
// Do not edit this file directly. Edit public/config/deployment.json instead.

export const deploymentConfig = {
  mode: '${config.mode}',
  useWindowLocation: ${useWindowLocation},
  useNavigate: ${useNavigate},
} as const;

export type DeploymentMode = typeof deploymentConfig.mode;
`;

  const configDir = path.dirname(outputPath);
  if (!fs.existsSync(configDir)) {
    fs.mkdirSync(configDir, { recursive: true });
  }

  fs.writeFileSync(outputPath, tsContent, 'utf-8');

  console.log(`✓ Generated deployment configuration: ${config.mode} mode`);
  console.log(`  - Use window.location.href: ${useWindowLocation}`);
  console.log(`  - Use navigate: ${useNavigate}`);
} catch (error) {
  console.error('✘ Error generating deployment configuration:', error.message);
  process.exit(1);
}
